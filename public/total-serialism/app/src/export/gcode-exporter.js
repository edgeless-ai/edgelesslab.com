/**
 * G-code Exporter for Pen Plotters
 * Converts SVG paths to G-code for various plotter types
 */

class GCodeExporter {
  constructor(config = {}) {
    // Default configuration
    this.config = {
      // Plotter type: 'axidraw', 'generic', 'grbl'
      plotterType: config.plotterType || 'generic',
      
      // Movement speeds (mm/min)
      moveSpeed: config.moveSpeed || 3000,
      drawSpeed: config.drawSpeed || 1500,
      
      // Pen positions (mm)
      penUpHeight: config.penUpHeight || 5,
      penDownHeight: config.penDownHeight || 0,
      
      // Work area (mm)
      width: config.width || 210,  // A4 width
      height: config.height || 297, // A4 height
      
      // Origin position
      homeX: config.homeX || 0,
      homeY: config.homeY || 0,
      
      // Optimization
      minimizePenLifts: config.minimizePenLifts !== false,
      
      // Output precision
      precision: config.precision || 3,
      
      // Comments in output
      includeComments: config.includeComments !== false
    };
  }

  /**
   * Export paths to G-code
   * @param {Array} paths - Array of path objects with points
   * @param {Object} options - Export options
   * @returns {string} G-code string
   */
  export(paths, options = {}) {
    const config = { ...this.config, ...options };
    const gcode = [];
    
    // Header
    gcode.push(...this.generateHeader(config));
    
    // Initialize
    gcode.push(...this.generateInitialization(config));
    
    // Process paths
    const optimizedPaths = config.minimizePenLifts ? 
      this.optimizePaths(paths) : paths;
    
    for (const path of optimizedPaths) {
      gcode.push(...this.generatePath(path, config));
    }
    
    // Footer
    gcode.push(...this.generateFooter(config));
    
    return gcode.join('\n');
  }

  /**
   * Generate G-code header
   */
  generateHeader(config) {
    const header = [];
    
    if (config.includeComments) {
      header.push(
        '; G-code generated by Pen Plotter Art',
        `; Plotter type: ${config.plotterType}`,
        `; Work area: ${config.width}mm x ${config.height}mm`,
        `; Generated: ${new Date().toISOString()}`,
        ''
      );
    }
    
    // Units and positioning
    header.push(
      'G21 ; Set units to millimeters',
      'G90 ; Absolute positioning',
      'G94 ; Feed rate mode: units per minute'
    );
    
    return header;
  }

  /**
   * Generate initialization commands
   */
  generateInitialization(config) {
    const init = [];
    
    // Home if supported
    if (config.plotterType === 'grbl') {
      init.push('$H ; Home all axes');
    }
    
    // Move to home position with pen up
    init.push(
      `G0 Z${this.formatNumber(config.penUpHeight)}`,
      `G0 X${this.formatNumber(config.homeX)} Y${this.formatNumber(config.homeY)} F${config.moveSpeed}`
    );
    
    return init;
  }

  /**
   * Generate G-code for a single path
   */
  generatePath(path, config) {
    const commands = [];
    
    if (config.includeComments && path.id) {
      commands.push(`; Path: ${path.id}`);
    }
    
    // Move to start position with pen up
    if (path.points && path.points.length > 0) {
      const start = this.transformPoint(path.points[0], config);
      
      // Pen up
      commands.push(`G0 Z${this.formatNumber(config.penUpHeight)}`);
      
      // Move to start
      commands.push(
        `G0 X${this.formatNumber(start.x)} Y${this.formatNumber(start.y)} F${config.moveSpeed}`
      );
      
      // Pen down
      commands.push(`G1 Z${this.formatNumber(config.penDownHeight)} F${config.drawSpeed / 2}`);
      
      // Draw path
      for (let i = 1; i < path.points.length; i++) {
        const point = this.transformPoint(path.points[i], config);
        
        // Check bounds
        if (this.isInBounds(point, config)) {
          commands.push(
            `G1 X${this.formatNumber(point.x)} Y${this.formatNumber(point.y)} F${config.drawSpeed}`
          );
        } else if (config.includeComments) {
          commands.push(`; Point out of bounds: ${point.x}, ${point.y}`);
        }
      }
      
      // Pen up after path
      commands.push(`G0 Z${this.formatNumber(config.penUpHeight)}`);
    }
    
    return commands;
  }

  /**
   * Generate G-code footer
   */
  generateFooter(config) {
    const footer = [];
    
    // Return to home
    footer.push(
      '',
      '; End of plot',
      `G0 Z${this.formatNumber(config.penUpHeight)}`,
      `G0 X${this.formatNumber(config.homeX)} Y${this.formatNumber(config.homeY)} F${config.moveSpeed}`
    );
    
    // Plotter-specific end commands
    switch (config.plotterType) {
      case 'axidraw':
        footer.push('M18 ; Disable motors');
        break;
      case 'grbl':
        footer.push('M5 ; Stop spindle (pen servo)');
        footer.push('M2 ; End program');
        break;
      default:
        footer.push('M2 ; End program');
    }
    
    return footer;
  }

  /**
   * Transform point from SVG coordinates to plotter coordinates
   */
  transformPoint(point, config) {
    // Assuming SVG coordinates are in pixels, scale to mm
    // This should be adjusted based on actual SVG viewBox
    const scale = config.width / 1000; // Adjust based on your needs
    
    return {
      x: point.x * scale,
      y: point.y * scale
    };
  }

  /**
   * Check if point is within plotter bounds
   */
  isInBounds(point, config) {
    return point.x >= 0 && 
           point.x <= config.width && 
           point.y >= 0 && 
           point.y <= config.height;
  }

  /**
   * Format number with precision
   */
  formatNumber(value) {
    return value.toFixed(this.config.precision);
  }

  /**
   * Optimize path order to minimize pen travel
   */
  optimizePaths(paths) {
    if (paths.length <= 1) return paths;
    
    const optimized = [];
    const remaining = [...paths];
    
    // Start with first path
    optimized.push(remaining.shift());
    
    // Greedy nearest neighbor
    while (remaining.length > 0) {
      const lastPath = optimized[optimized.length - 1];
      const lastPoint = lastPath.points[lastPath.points.length - 1];
      
      let nearestIndex = 0;
      let nearestDistance = Infinity;
      
      // Find nearest path start
      remaining.forEach((path, index) => {
        if (path.points && path.points.length > 0) {
          const distance = this.distance(lastPoint, path.points[0]);
          if (distance < nearestDistance) {
            nearestDistance = distance;
            nearestIndex = index;
          }
        }
      });
      
      optimized.push(remaining.splice(nearestIndex, 1)[0]);
    }
    
    return optimized;
  }

  /**
   * Calculate distance between two points
   */
  distance(p1, p2) {
    const dx = p2.x - p1.x;
    const dy = p2.y - p1.y;
    return Math.sqrt(dx * dx + dy * dy);
  }

  /**
   * Estimate plotting time
   */
  estimateTime(paths) {
    let totalDistance = 0;
    let penMoves = 0;
    
    const optimizedPaths = this.config.minimizePenLifts ? 
      this.optimizePaths(paths) : paths;
    
    for (let i = 0; i < optimizedPaths.length; i++) {
      const path = optimizedPaths[i];
      
      // Pen move to start
      if (i > 0) {
        const prevPath = optimizedPaths[i - 1];
        const prevEnd = prevPath.points[prevPath.points.length - 1];
        const currentStart = path.points[0];
        penMoves += this.distance(prevEnd, currentStart);
      }
      
      // Drawing distance
      for (let j = 1; j < path.points.length; j++) {
        totalDistance += this.distance(path.points[j - 1], path.points[j]);
      }
    }
    
    // Time calculation (in seconds)
    const drawTime = (totalDistance / this.config.drawSpeed) * 60;
    const moveTime = (penMoves / this.config.moveSpeed) * 60;
    const penLiftTime = optimizedPaths.length * 0.5; // 0.5s per pen lift
    
    return {
      drawTime,
      moveTime,
      penLiftTime,
      totalTime: drawTime + moveTime + penLiftTime,
      totalDistance,
      penMoves,
      pathCount: optimizedPaths.length
    };
  }
}

// Export for use in other modules
if (typeof module !== 'undefined' && module.exports) {
  module.exports = GCodeExporter;
}