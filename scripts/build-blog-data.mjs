#!/usr/bin/env node
/**
 * Prebuild: Read markdown posts from content/blog/ and generate src/lib/blog.ts.
 *
 * Source of truth: content/blog/{slug}.md (YAML frontmatter + markdown body)
 * Output: src/lib/blog.ts (TypeScript array, committed or generated at build time)
 *
 * Usage:
 *   node scripts/build-blog-data.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const BLOG_DIR = path.join(ROOT, "content/blog");
const OUTPUT = path.join(ROOT, "src/lib/blog.ts");

function escapeTemplate(str) {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
}

function run() {
  if (!fs.existsSync(BLOG_DIR)) {
    console.error(`Blog content directory not found: ${BLOG_DIR}`);
    process.exit(1);
  }

  const files = fs
    .readdirSync(BLOG_DIR)
    .filter((f) => f.endsWith(".md") && !f.startsWith("."));

  const posts = [];

  for (const file of files) {
    const raw = fs.readFileSync(path.join(BLOG_DIR, file), "utf-8");
    const { data, content } = matter(raw);

    const required = ["slug", "title", "description", "date", "tags", "readTime"];
    const missing = required.filter((k) => data[k] === undefined);
    if (missing.length) {
      console.warn(`  Skipping ${file}: missing ${missing.join(", ")}`);
      continue;
    }

    const parts = [];
    parts.push(`    slug: ${JSON.stringify(data.slug)}`);
    parts.push(`    title: ${JSON.stringify(data.title)}`);
    parts.push(`    description: ${JSON.stringify(data.description)}`);
    parts.push(`    date: ${JSON.stringify(data.date)}`);
    parts.push(`    tags: [${data.tags.map((t) => JSON.stringify(t)).join(", ")}]`);
    parts.push(`    readTime: ${JSON.stringify(data.readTime)}`);
    if (data.editorial) parts.push("    editorial: true");
    if (data.isLaunch) parts.push("    isLaunch: true");
    if (data.productSlug) parts.push(`    productSlug: ${JSON.stringify(data.productSlug)}`);
    if (data.ctaHook) parts.push(`    ctaHook: ${JSON.stringify(data.ctaHook)}`);
    if (data.status) parts.push(`    status: ${JSON.stringify(data.status)}`);
    parts.push(`    content: \`\n${escapeTemplate(content.trim())}\n    \`.trim()`);

    const postTs = `  {\n${parts.join(",\n")},\n  }`;
    posts.push({
      date: data.date,
      ts: postTs,
    });
  }

  // Sort by date descending (most recent first)
  posts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());

  const output = `\
// Auto-generated from content/blog/*.md by scripts/build-blog-data.mjs
// Do not edit this file directly — edit the markdown files instead.

export interface BlogPost {
  slug: string;
  title: string;
  description: string;
  date: string;
  tags: string[];
  readTime: string;
  content: string;
  productSlug?: string;
  /** True only for posts that announce a product launch (not editorial posts that happen to link a product). */
  isLaunch?: boolean;
  /** Two-column layout with sticky TOC sidebar. For longer, narrative posts. */
  editorial?: boolean;
  /** One-line hook for the companion product CTA. Pain-point framing, not generic. */
  ctaHook?: string;
  /** Posts with status "draft" are excluded from the public site. Omit or set "published" to publish. */
  status?: "draft" | "published";
}

const allPosts: BlogPost[] = [
${posts.map((p) => p.ts).join(",\n")},
];

export const posts: BlogPost[] = allPosts.filter(
  (p) => p.status !== "draft",
);
`;

  fs.writeFileSync(OUTPUT, output, "utf-8");
  console.log(`Generated ${OUTPUT} with ${posts.length} posts from content/blog/`);
}

run();
